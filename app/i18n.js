function translateValue(value, language) {
  const leading = value.match(/^\s*/)?.[0] ?? "";
  const trailing = value.match(/\s*$/)?.[0] ?? "";
  const core = value.trim();
  const dictionary = language === "en" ? englishByRussian : russianByEnglish;
  return `${leading}${dictionary[core] ?? core}${trailing}`;
}

export function applyInterfaceLanguage(language, root = document) {
  const elementRoot = root.documentElement ?? root;
  const body = root.body ?? elementRoot;
  const walker = root.createTreeWalker(
    body,
    NodeFilter.SHOW_TEXT,
    {
      acceptNode(node) {
        if (!node.nodeValue?.trim()) return NodeFilter.FILTER_REJECT;
        if (node.parentElement?.closest("[data-language-toggle]")) {
          return NodeFilter.FILTER_REJECT;
        }
        if (["SCRIPT", "STYLE"].includes(node.parentElement?.tagName)) {
          return NodeFilter.FILTER_REJECT;
        }
        return NodeFilter.FILTER_ACCEPT;
      },
    },
  );

  const nodes = [];
  while (walker.nextNode()) nodes.push(walker.currentNode);
  nodes.forEach((node) => {
    node.nodeValue = translateValue(node.nodeValue, language);
  });

  body.querySelectorAll("[aria-label], [title], [placeholder]").forEach((node) => {
    ["aria-label", "title", "placeholder"].forEach((attribute) => {
      const value = node.getAttribute(attribute);
      if (value) node.setAttribute(attribute, translateValue(value, language));
    });
  });

  if (root.documentElement) root.documentElement.lang = language;
  if (root.title) root.title = translateValue(root.title, language);
  const description = root.querySelector?.('meta[name="description"]');
  if (description?.content) {
    description.content = translateValue(description.content, language);
  }
}
